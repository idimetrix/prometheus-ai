import { createLogger } from "@prometheus/logger";
import type { SetupProjectEnvironmentData } from "@prometheus/queue";
import { EventPublisher } from "@prometheus/queue";

const logger = createLogger("queue-worker:setup-environment");
const publisher = new EventPublisher();

const SANDBOX_MANAGER_URL =
  process.env.SANDBOX_MANAGER_URL ?? "http://localhost:4006";

// ---------------------------------------------------------------------------
// Sandbox exec helper
// ---------------------------------------------------------------------------

interface ExecResult {
  exitCode: number;
  stderr: string;
  stdout: string;
}

async function execInSandbox(
  sandboxId: string,
  command: string,
  timeoutMs = 120_000
): Promise<ExecResult> {
  const response = await fetch(
    `${SANDBOX_MANAGER_URL}/sandbox/${sandboxId}/exec`,
    {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ command, timeout: timeoutMs }),
      signal: AbortSignal.timeout(timeoutMs + 5000),
    }
  );

  if (!response.ok) {
    const text = await response.text();
    return { exitCode: 1, stdout: "", stderr: text };
  }

  return (await response.json()) as ExecResult;
}

async function readSandboxFile(
  sandboxId: string,
  filePath: string
): Promise<string | null> {
  try {
    const result = await execInSandbox(
      sandboxId,
      `cat "${filePath}" 2>/dev/null`,
      10_000
    );
    return result.exitCode === 0 ? result.stdout : null;
  } catch {
    return null;
  }
}

// ---------------------------------------------------------------------------
// Step results
// ---------------------------------------------------------------------------

export interface SetupResult {
  buildVerified: boolean;
  dependenciesInstalled: boolean;
  envFileCreated: boolean;
  stackDetected: boolean;
  techStack: {
    buildCommand: string;
    devCommand: string;
    frameworks: string[];
    isMonorepo: boolean;
    languages: string[];
    packageManager: string;
    suggestedPreset: string;
    testCommand: string;
  } | null;
}

// ---------------------------------------------------------------------------
// Step helpers (extracted to reduce cognitive complexity)
// ---------------------------------------------------------------------------

async function detectStack(
  sandboxId: string,
  projectId: string
): Promise<SetupResult["techStack"]> {
  const listResult = await execInSandbox(
    sandboxId,
    'find /workspace -maxdepth 3 -type f ! -path "*/node_modules/*" ! -path "*/.git/*" ! -path "*/vendor/*" ! -path "*/target/*" 2>/dev/null | head -500'
  );

  const files = listResult.stdout
    .split("\n")
    .filter(Boolean)
    .map((f) =>
      f.startsWith("/workspace/") ? f.slice("/workspace/".length) : f
    );

  const contentMap: Record<string, string> = {};
  const configFiles = [
    "package.json",
    "requirements.txt",
    "pyproject.toml",
    "Gemfile",
    "composer.json",
  ];

  for (const cf of configFiles) {
    if (files.includes(cf)) {
      const content = await readSandboxFile(sandboxId, `/workspace/${cf}`);
      if (content) {
        contentMap[cf] = content;
      }
    }
  }

  const { detectTechStack } = (await import(
    "@prometheus/config-stacks"
  )) as typeof import("@prometheus/config-stacks");

  const stack = await detectTechStack(files, contentMap);
  logger.info({ projectId, stack }, "Tech stack detected");
  return stack;
}

async function setupEnvFile(
  sandboxId: string,
  projectId: string
): Promise<boolean> {
  const templateCheck = await execInSandbox(
    sandboxId,
    "ls -1 /workspace/.env.example /workspace/.env.template 2>/dev/null || true",
    10_000
  );

  const templateFile = templateCheck.stdout
    .trim()
    .split("\n")
    .filter(Boolean)[0];

  if (templateFile) {
    const copyResult = await execInSandbox(
      sandboxId,
      `cp "${templateFile}" /workspace/.env`,
      10_000
    );
    return copyResult.exitCode === 0;
  }

  const envContent = [
    "# Auto-generated by Prometheus",
    "NODE_ENV=development",
    "PORT=3000",
    "DATABASE_URL=postgresql://postgres:postgres@localhost:5432/app",
    "REDIS_URL=redis://localhost:6379",
  ].join("\n");

  const writeResult = await execInSandbox(
    sandboxId,
    `cat > /workspace/.env << 'EOF'\n${envContent}\nEOF`,
    10_000
  );

  logger.info(
    { projectId, envFileCreated: writeResult.exitCode === 0 },
    "Environment file setup complete"
  );
  return writeResult.exitCode === 0;
}

function getInstallCommand(packageManager: string): string {
  const commands: Record<string, string> = {
    pnpm: "pnpm install",
    yarn: "yarn install",
    bun: "bun install",
    pip: "pip install -r requirements.txt",
    poetry: "poetry install",
    cargo: "cargo build",
    go: "go mod download",
    bundler: "bundle install",
    composer: "composer install",
  };
  return commands[packageManager] ?? "npm install";
}

