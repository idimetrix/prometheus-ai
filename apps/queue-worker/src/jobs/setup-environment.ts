async function setupGitAuth(
  sandboxId: string,
  projectId: string,
  token: string,
  host?: string
): Promise<boolean> {
  try {
    const response = await fetch(
      `${SANDBOX_MANAGER_URL}/sandbox/${sandboxId}/git`,
      {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          operation: "setupAuth",
          token,
          host: host ?? "github.com",
        }),
        signal: AbortSignal.timeout(15_000),
      }
    );

    if (!response.ok) {
      const text = await response.text();
      logger.error(
        { projectId, sandboxId, error: text },
        "Git auth setup failed"
      );
      return false;
    }

    const result = (await response.json()) as { success: boolean };
    return result.success;
  } catch (err) {
    const msg = err instanceof Error ? err.message : String(err);
    logger.error({ projectId, sandboxId, error: msg }, "Git auth setup threw");
    return false;
  }
}

export async function processSetupEnvironment(
  data: SetupProjectEnvironmentData,
  onProgress?: (progress: Record<string, unknown>) => void
): Promise<SetupResult> {
  const { projectId, sandboxId, orgId } = data;

  logger.info({ projectId, sandboxId }, "Starting project environment setup");

  const result: SetupResult = {
    stackDetected: false,
    dependenciesInstalled: false,
    envFileCreated: false,
    buildVerified: false,
    techStack: null,
  };

  // ---- Step 0: Configure git authentication if token provided ----
  if (data.gitToken) {
    onProgress?.({ step: "git_auth", status: "running" });
    const authOk = await setupGitAuth(
      sandboxId,
      projectId,
      data.gitToken,
      data.gitHost
    );
    onProgress?.({
      step: "git_auth",
      status: authOk ? "complete" : "failed",
    });
    if (authOk) {
      logger.info({ projectId, sandboxId }, "Git authentication configured");
    }
  }

  // ---- Step 1: Detect tech stack ----
  onProgress?.({ step: "detect_stack", status: "running" });
  try {
    result.techStack = await detectStack(sandboxId, projectId);
    result.stackDetected = true;
    onProgress?.({
      step: "detect_stack",
      status: "complete",
      stack: result.techStack,
    });
  } catch (err) {
    logger.error({ projectId, err }, "Stack detection failed");
    onProgress?.({ step: "detect_stack", status: "failed" });
  }

  // ---- Step 2: Setup .env from template ----
  onProgress?.({ step: "setup_env", status: "running" });
  try {
    result.envFileCreated = await setupEnvFile(sandboxId, projectId);
    onProgress?.({
      step: "setup_env",
      status: result.envFileCreated ? "complete" : "skipped",
    });
  } catch (err) {
    logger.warn({ projectId, err }, "Env setup failed, continuing");
    onProgress?.({ step: "setup_env", status: "failed" });
  }

  // ---- Step 3: Install dependencies ----
  if (result.techStack) {
    onProgress?.({ step: "install_deps", status: "running" });
    try {
      const pm = result.techStack.packageManager;
      const installCmd = getInstallCommand(pm);
      const installResult = await execInSandbox(
        sandboxId,
        `cd /workspace && ${installCmd}`,
        180_000
      );
      result.dependenciesInstalled = installResult.exitCode === 0;
      logger.info(
        { projectId, pm, success: result.dependenciesInstalled },
        "Dependency installation complete"
      );
      onProgress?.({
        step: "install_deps",
        status: result.dependenciesInstalled ? "complete" : "failed",
        packageManager: pm,
      });
    } catch (err) {
      logger.warn({ projectId, err }, "Dependency install failed, continuing");
      onProgress?.({ step: "install_deps", status: "failed" });
    }
  }

  // ---- Step 4: Verify build ----
  if (result.techStack?.buildCommand && result.dependenciesInstalled) {
    onProgress?.({ step: "verify_build", status: "running" });
    try {
      const buildResult = await execInSandbox(
        sandboxId,
        `cd /workspace && ${result.techStack.buildCommand}`,
        180_000
      );
      result.buildVerified = buildResult.exitCode === 0;
      logger.info(
        {
          projectId,
          buildCommand: result.techStack.buildCommand,
          success: result.buildVerified,
        },
        "Build verification complete"
      );
      onProgress?.({
        step: "verify_build",
        status: result.buildVerified ? "complete" : "failed",
      });
    } catch (err) {
      logger.warn({ projectId, err }, "Build verification failed, continuing");
      onProgress?.({ step: "verify_build", status: "failed" });
    }
  }

  // ---- Publish completion event ----
  try {
    await publisher.publishFleetEvent(orgId, {
      type: "setup_environment_complete",
      data: { projectId, sandboxId, ...result },
      timestamp: new Date().toISOString(),
    });
  } catch {
    // Non-critical
  }

  logger.info({ projectId, ...result }, "Project environment setup complete");
  return result;
}
